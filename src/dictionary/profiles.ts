const uniqueWords = (words: string[]) => {
    const seen = new Set<string>();
    const result: string[] = [];

    for (const word of words) {
        const normalized = normalizeArabicForComparison(word);
        if (!normalized || seen.has(normalized)) {
            continue;
        }

        seen.add(normalized);
        result.push(word);
    }

    return result;
};

